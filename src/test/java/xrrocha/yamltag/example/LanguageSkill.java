package xrrocha.yamltag.example;

public class LanguageSkill {
    private SkillLevel speaking;
    private SkillLevel reading;
    private SkillLevel writing;
    
    public SkillLevel getSpeaking() {
        return speaking;
    }
    public void setSpeaking(SkillLevel speaking) {
        this.speaking = speaking;
    }
    public SkillLevel getReading() {
        return reading;
    }
    public void setReading(SkillLevel reading) {
        this.reading = reading;
    }
    public SkillLevel getWriting() {
        return writing;
    }
    public void setWriting(SkillLevel writing) {
        this.writing = writing;
    }
}
